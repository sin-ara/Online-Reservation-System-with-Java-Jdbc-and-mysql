        if(ans==JOptionPane.YES_OPTION){
            try{
                stmt = con.createStatement();
                String query = "UPDATE stations SET Sname = \"" + SnameTF.getText()+"\", Category = \"" + CategoryTF.getText() + "\" , zone = \"" + ZoneTF.getText() + "\" ,division = \"" +Division.getText() +"\" , remarks = \""+ RemarksTF.getText()+"\" WHERE stn_code = \"" + StncodeTF.getText() + "\";";
                stmt.execute(query);
                JOptionPane.showMessageDialog(null, "Record successfully updated.");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString());
            }
            StncodeTF.setText("");
            SnameTF.setText("");
            CategoryTF.setText("");
            ZoneTF.setText("");
            Division.setText("");
            RemarksTF.setText("");
            updBtn.setEnabled(false);
            delBtn.setEnabled(false);
        }
