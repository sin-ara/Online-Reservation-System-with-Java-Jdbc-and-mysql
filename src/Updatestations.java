    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed

        try{
            Class.forName("java.sql.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/irctc","root","ap");
            stmt = con.createStatement();
            String query = "SELECT * from stations WHERE stn_code = \"" + StncodeTF.getText() + "\";";
            rs = stmt.executeQuery(query);
            if (rs.next()){
                String un = rs.getString("stn_code");
                String pass = rs.getString("Sname");
                String cate = rs.getString("Category");
                String div = rs.getString("Division");
                String zone = rs.getString("zone");
                String remarks = rs.getString("remarks");
                SnameTF.setText(pass);
                CategoryTF.setText(cate);
                ZoneTF.setText(div);
                Division.setText(zone);
                RemarksTF.setText(remarks);
                updBtn.setEnabled(true);
                delBtn.setEnabled(true);
                SnameTF.setEnabled(true);
                CategoryTF.setEnabled(true);
                ZoneTF.setEnabled(true);
                Division.setEnabled(true);
                RemarksTF.setEnabled(true);
            } else{
                JOptionPane.showMessageDialog(null,"No Such Record Found.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString() );
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_loadBtnActionPerformed

