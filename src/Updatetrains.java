        if(ans==JOptionPane.YES_OPTION){
            try{
                stmt = con.createStatement();
                String query = "UPDATE trains SET Tname = \"" + TnameTF.getText()+"\", Type = \"" + TypeTF.getText() + "\" , maxseats = \"" + MaxsTF.getText() + " \" ,comments = \"" +CommTF.getText() + "\" WHERE train_no = \"" + Train_noTF.getText() + "\";";
                stmt.execute(query);
                JOptionPane.showMessageDialog(null, "Record successfully updated.");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString());
            }
            Train_noTF.setText("");
            TnameTF.setText("");
            TypeTF.setText("");
            MaxsTF.setText("");
            CommTF.setText("");
            updBtn.setEnabled(false);
            delBtn.setEnabled(false);
        }
