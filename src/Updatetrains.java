    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed

        try{
            Class.forName("java.sql.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/irctc","root","ap");
            stmt = con.createStatement();
            String query = "SELECT * from trains WHERE Train_no = \"" + Train_noTF.getText() + "\";";
            rs = stmt.executeQuery(query);
            if (rs.next()){
                String un = rs.getString("Train_no");
                String pass = rs.getString("Tname");
                String cate = rs.getString("Type");
                String maxs = rs.getString("Maxseats");
                String comments = rs.getString("comments");
                TnameTF.setText(pass);
                TypeTF.setText(cate);
                MaxsTF.setText(maxs);
                CommTF.setText(comments);
                updBtn.setEnabled(true);
                delBtn.setEnabled(true);
                TnameTF.setEnabled(true);
                TypeTF.setEnabled(true);
                MaxsTF.setEnabled(true);
                CommTF.setEnabled(true);
            } else{
                JOptionPane.showMessageDialog(null,"No Such Record Found.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString() );
        }
        // TODO add your handling code here:
}//GEN-LAST:event_loadBtnActionPerformed

